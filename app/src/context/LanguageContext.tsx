interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
  toggleLanguage: () => void;
  supportedLanguages: LanguageInfo[];
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>('en');

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('earthworm-language', lang);
  }, []);

  const toggleLanguage = useCallback(() => {
    const currentIndex = supportedLanguages.findIndex(l => l.code === language);
    const nextIndex = (currentIndex + 1) % supportedLanguages.length;
    setLanguage(supportedLanguages[nextIndex].code);
  }, [language]);

  const t = useCallback(
    (key: string): string => {
      return translations[key]?.[language] || translations[key]?.['en'] || key;
    },
    [language]
  );

  // Load saved language on mount
  React.useEffect(() => {
    const saved = localStorage.getItem('earthworm-language') as Language;
    if (saved && supportedLanguages.some(l => l.code === saved)) {
      setLanguageState(saved);
    }
  }, []);

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, toggleLanguage, supportedLanguages }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
